package com.sniper.springmvc.searchUtil;

public class MainHomeSearch extends BaseSearch {

	private String id;
	private int count;
	private String office;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getOffice() {
		return office;
	}

	public void setOffice(String office) {
		this.office = office;
	}
}
